package com.jdbc;

public class Books {
private int id;
private String name;
private String genre;
private int lambai;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public String getGenre() {
	return genre;
}
public void setGenre(String genre) {
	this.genre = genre;
}
public int getLambai() {
	return lambai;
}
public void setLambai(int lambai) {
	this.lambai = lambai;
}


}
