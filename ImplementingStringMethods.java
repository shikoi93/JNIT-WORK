public class ImplementingStringMethods {
	public static void main(String[] ar){
	String a = "Sky";
	String b = "is blue.";
	System.out.println("A String is = " +a);
	System.out.println("B String is = " +b);
	System.out.println("Concatinating a and b = " + a.concat(b));
	System.out.println("Length of a = " + a.length());
	System.out.println("index of u = " + b.indexOf('u'));
	System.out.println("UpperCase of b = " + b.toUpperCase());
	System.out.println("LowerCase of a = " + a.toLowerCase());
	System.out.println("B ends with = " + b.endsWith("."));
}
}




